// Hàm hiển thị sản phẩm theo trang
function displayProductPerPage(page) {
  window.scrollTo(0, 0); // Cuộn lên đầu trang

  currentPage = page;
  const startIndex = (page - 1) * productPerPage;
  const endIndex = startIndex + productPerPage;
  const cutArr = currentProductList.slice(startIndex, endIndex); // Lấy phần tử cho trang hiện tại

  display(cutArr); // Hiển thị danh sách sản phẩm cắt từ currentProductList
  productPagination(); // Cập nhật phân trang dựa trên currentProductList
}

// Hàm tạo phân trang
function productPagination() {
  const totalProduct = currentProductList.length;
  const totalPage = Math.ceil(totalProduct / productPerPage); // Tổng số trang

  let paginationHTML = "";
  for (let i = 1; i <= totalPage; i++) {
    const isActive = i === currentPage;
    paginationHTML += `
      <button class="btn-page ${
        isActive ? "active" : ""
      }" onclick="displayProductPerPage(${i})">
        ${i}
      </button>
    `;
  }
  document.getElementById("pagination").innerHTML = paginationHTML;
}

// Hàm hiển thị sản phẩm theo thương hiệu
function displayProductByBrand(obj) {
  currentProductList = listProducts.filter(
    (product) => product.brandId == obj.id
  );
  currentPage = 1;
  displayProductPerPage(currentPage); // Hiển thị sản phẩm theo trang đầu
}

// Hàm lọc sản phẩm theo khoảng giá
function filterAboutPrice() {
  const maxPrice = Number(document.getElementById("rangeSlider").value);
  const minPrice = 2000000; // Thay đổi tùy theo yêu cầu khoảng giá thấp nhất

  currentProductList = currentProductList.filter(
    (product) => product.price >= minPrice && product.price <= maxPrice
  );

  if (currentProductList.length === 0) {
    alert("Danh sách rỗng");
  } else {
    currentPage = 1; // Đặt lại trang hiện tại về 1
    displayProductPerPage(currentPage); // Hiển thị sản phẩm đã lọc theo trang đầu
  }
}

